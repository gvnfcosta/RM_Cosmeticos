class Product {
  String id;
  String code;
  String name;
  String description;
  String category;
  String subCategory;
  String offer;
  double price;
  String unit;
  bool show;
  String imageUrl;

  Product({
    required this.id,
    required this.code,
    required this.name,
    required this.description,
    required this.category,
    required this.subCategory,
    required this.offer,
    required this.price,
    required this.unit,
    required this.show,
    required this.imageUrl,
  });
}
